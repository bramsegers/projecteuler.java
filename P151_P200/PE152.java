    int COUNT;
    int[] P, T;
    List<Integer> NUMS = new ArrayList<>();

    void solve(int nMax) {
        long start = System.currentTimeMillis();
        int[] nums = {2, 3, 4, 5, 6, 7, 8, 9, 10, 12, 13, 14, 15, 18, 20, 21,
            24, 26, 28, 30, 35, 36, 39, 40, 42, 45, 52, 56, 60, 63, 65, 70, 72, 78};
        int i = 0, n;
        while (i < nums.length && (n = nums[i]) <= nMax) {
            NUMS.add(n);
            i++;
        }
        int lcm = 1;
        for (int j : NUMS) {
            lcm = lcm(lcm, j * j);
        }
        P = new int[NUMS.size()];
        T = new int[NUMS.size()];
        for (i = NUMS.size() - 1; i >= 0; i--) {
            P[i] = (int) (lcm / NUMS.get(i) / NUMS.get(i));
            T[i] = i == NUMS.size() - 1 ? 0 : T[i + 1] + P[i + 1];
        }
        solve(lcm / 2 - P[0], P[0], 0, 1);
        //solve(lcm / 2, 0, 0, 0);
        long end = System.currentTimeMillis();
        System.out.format("P(%d)=%d%nElapsed:%dms%n", nMax, COUNT, end - start);
    }

    int lcm(int a, int b) {
        return a * (b / gcd(a, b));
    }

    int gcd(int a, int b) {
        return b == 0 ? a : gcd(b, a % b);
    }

    void solve(int todo, int done, int n, long path) {
        if (todo == 0) {
            System.out.println(++COUNT + ":" + path(path));
        } else if (n + 1 < NUMS.size() && todo > 0 && todo <= done + T[n]) {
            n++;
            solve(todo - P[n], done + P[n], n, path + (1L << n));
            solve(todo, done, n, path);
        }
    }

    String path(long path) {
        String s = "";
        int i = 0;
        while (path > 0) {
            if ((path & 1) == 1) {
                s += " " + NUMS.get(i);
            }
            path >>= 1;
            i++;
        }
        return s;
    }

